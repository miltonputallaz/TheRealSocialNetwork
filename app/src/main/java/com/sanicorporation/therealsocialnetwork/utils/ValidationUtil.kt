package com.sanicorporation.therealsocialnetwork.utils

import android.util.Patterns

object ValidationUtil{
    val mailRegex = Regex("(?:[a-z0-9!#\$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#\$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])")

    fun emailIsValid(email : String?): Boolean{
        return email != null && email.length > 9 && mailRegex.matches(email)
    }

    fun passwordAreValid(pass : String?, confirmPassword : String?) : Boolean{
        return passwordIsValid(pass) && passwordIsValid(confirmPassword) && pass == confirmPassword
    }

    fun passwordIsValid(pass : String?) : Boolean {
        return pass != null && pass.length >= 6
    }

}
